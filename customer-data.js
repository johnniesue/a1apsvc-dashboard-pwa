/**
 * Initialize customer data on page load
 */
function initializeCustomerData() {
    // Check if customers are already loaded
    const existingCustomers = JSON.parse(localStorage.getItem('a1apsvc_customers') || '[]');
    
    // If no customers exist, load the pre-populated data
    if (existingCustomers.length === 0) {
        localStorage.setItem('a1apsvc_customers', JSON.stringify(A1APSVC_CUSTOMER_DATA));
        console.log('âœ… Loaded', A1APSVC_CUSTOMER_DATA.length, 'customers from Housecall Pro export');
        return A1APSVC_CUSTOMER_DATA;
    }
    
    return existingCustomers;
}

